                    WeChat was released in 2011 by Tencent, one of China’s biggest tech companies. With over 1.32 billion monthly users, 80 percent of China's total population is active on the app.Like WhatsApp and Messenger, WeChat was originally a messaging app, but it’s evolved into an all-in-one platform. Besides messaging and calling, users can shop online, pay bills, buy groceries, transfer money, make reservations, book taxis, and more.WeChat is the most popular social media app in China and other parts of Asia.</p>
