                   <h2 class="underline">Don’t keep your fears to yourself</h2>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    If you are worried about your safety or something that has happened to you online, urgently speak to an adult you trust. You can call 116 to the Human Rights Defender of Armenia. Learn more here. </p>
                     <h2 class="underline">Respect what other people are saying on social media <a id="down2"></a></h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                   Respect what other people are saying on social media, even if you do not agree with them. </p>
                      <h2 class="underline">You have the right to privacy <a id="down3"></a></h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    You have the right to privacy - and so do others. It is not okay to log into other people’s accounts or to use their phones or profiles without their permission. </p>
                      <h2 class="underline">Don’t spread rumors or post/share hurtful or embarrassing stories or photos <a id="down4"></a></h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    What may seem like a harmless joke to one person can be deeply hurtful to others. We all have the right to dignity and to be treated with respect.</p>
                      <h2 class="underline">Think twice before you click ‘send’ <a id="down5"></a> </h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    Especially if you’re upset or angry. Once you share a photo or a video it’s hard to control what happens to it and who sees it. Taking it down is nearly impossible.</p>
                      <h2 class="underline">You can report it <a id="down6"></a></h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                   If you’ve seen something on social media that made you upset or hurt you, you can report it. On many social media platforms, you can report a specific post or photo if it is against the community standards of the social media platform. Most social media platforms let you “block” a person so that you don’t see their content and they cannot contact you or send you messages. You can learn more by following the links at the end of this page. </p>
                      <h2 class="underline">You can change the privacy settings on your social media platforms <a id="down7"></a></h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    To help you control who sees your information, photos and videos. Think carefully and what you share with whom. It may seem obvious but don’t share personal information like your address, phone number or bank details. If your privacy settings are not secure anyone can see this information. </p>
                      <h2 class="underline">Don’t feel pressured to accept random friend requests. <a id="down8"></a></h2>
                     <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    Making new friends is great but before you accept a person you should have a look at their profile - try to see who they are. Do you have friends in common? Are you from the same town? Don’t feel pressured to accept random friend requests. Check your privacy settings so that this person does not see any information that you don’t want them to see. Remember that sometimes people pretend to be someone they are not and it’s hard to know if they are telling the truth about who they are. </p>
                      <h2 class="underline">Be careful about any job offers that you receive online <a id="down9"></a></h2>
