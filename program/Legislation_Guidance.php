                               <h2 class="underline">Social Media Legislation and Guidance</h2>
                                        <p>It is virtually impossible to create a comprehensive list of all social media laws that are valid everywhere that the Internet reaches. The already long list is a constantly changing amalgamation of regulations, statutes and guidelines governing all manner of social media use and abuse. All of them should be taken seriously by every social media user. </p>
                               <h4 class="underline">National Laws</h4>
                                       <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                                       There are some laws that apply everywhere in the country. These include the Digital Millennium Copyright Act (DMCA) §512, Communications Decency Act § 230©(1), and FTC Act §5 15 U.S.C. 45. If a state law conflicts with a federal law, the federal law prevails. </p>
                                <h4 class="underline">State Laws</h4>
                                       <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                                     Every state has its own laws regarding social media and you could face both state and federal charges for the same offense. Some states have specific statutes applying to specific social media behavior, like cyberbullying, while others can prosecute using other statutes not specifically limited to social media. Penalties perpetrators face can vary significantly from state to state. </p>
                                <h4 class="underline">Guidelines</h4>
                                       <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                                      Don’t let the term fool you. Whether it is called a law, statute, regulation, or guideline, it comes with penalties attached. If a government agency provides “guidelines” for social media behavior, take them seriously. Action can be taken against you for violating guidelines much in the same way you can be prosecuted for violating a law or regulation. </p>
                                 <h4 class="underline">International Laws</h4>
                                       <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                                       Freedoms and rights we enjoy on social media in the United States can be controlled or forbidden in other countries. Some nations approach issues such as privacy far more seriously than we do in America. Foreign social media law is important because social media does not stop at international borders. Social media activity can be legal where it originates but illegal where it spreads.   </p>
