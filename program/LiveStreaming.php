                <h2>How it can be done in a safe environment</h2>
                    <h4 class="underline">Password protection</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    One way to control who has access to your live streams is to implement password protection. This way, you can distribute your password to the relevant people and ensure that no potentially sensitive content is accessed by the wrong people. While hackers have been known to find ways to access passwords, it’s a simple way to share your video content with a specific set of people.   </p>
            
              <h4 class="underline">Payment encryption</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                  If you’re live streaming an event, webinar, debate, or anything online in exchange for payment, it’s essential to make sure your customers' payments are protected. Fail to do so, and you risk finding yourself in tricky legal scenarios or losing out on potential customers if they don’t feel confident in providing their payment details. To secure your payment process for viewers, you should use SSL encryption. This uses an encryption algorithm to protect all information being passed between servers, including highly sensitive and personal information like payment details.   </p>
            
              <h4 class="underline">Geographic or domain restrictions</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    Domain restrictions allow you to control which specific domains have permission to embed your streams. Simply make a list of all the domains that are allowed access to your embed code, and any that aren’t on the list will be unable to play your videos on their site. To go one step further, you can place restrictions on the specific areas and locations that can play or view your live streams. If you only want to open your streams up to viewers in certain countries or areas, you can identify the relevant IP addresses and prevent foreign countries from tuning in.</p>
            
              <h4 class="underline">Copyright control</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                   A copyright is something that a content creator owns which prevents anyone else from using your videos, whether it's to publish, recreate, repurpose and more. By holding the copyright to a video, only you or your brand has the rights to that content, preventing others from benefiting from your ideas and hard work.</p>
