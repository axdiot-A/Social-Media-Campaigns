           <h2>Type Of Live Streams</h2>
           <h4 class="underline">TYPE ONE – IN-PERSON</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                    The first type of live stream we’ll talk about is physical, or in-person. This is where we set up in a physical venue, have presenters, speakers and hosts all on a stage, talking to a virtual audience who could be watching anywhere in the world. Participants on the stream might physically present on stage, or join via video call, so they can talk to the host of the event or do their own presentation. An example of this is the Birmingham Royal Ballet International Dance Week Festival live stream.  </p>
            <h4 class="underline">TYPE TWO – HYBRID</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                  The second type of live stream is a hybrid. Similarly to the first type of live stream, we set up in a physical location but add the layer of an audience in the room. For something like this, we would then have to consider adding extra sound and visual requirements, such as loud speakers and extra screens for the audience in the room. An example of this is the live stream we filmed with the Great Britain Judo team.  </p>
            <h4 class="underline">TYPE THREE – VIRTUAL</h4>
                   <p style="font-family: inherit; font-size:20px; margin-bottom: 20px; margin-right: 20px;">
                   The third type of live stream is going completely virtual. We have no physical presence and we can stream your virtual event from our offices. All speakers, presenters and hosts appear via video call, which we can then run through our software and then push out again through a virtual stage. We’ve used this variation where we have participants calling in from across the world.  </p>
